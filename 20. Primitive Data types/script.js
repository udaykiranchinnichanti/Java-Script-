//List of data types

let firstName = 'KIRAN'; // String

firstName = "UDAY"

let age = 26; // number or Integer

let isMale = true; // boolean

let dob; // undefined

let address = null; // Null

// Primitive data types - Value Data type

console.log(firstName, "String Type");
console.log(age, "Number Type");
console.log(isMale, "Boolean Type");
console.log(dob, "Undefined Type");
console.log(address, "Null Type");

console.log(typeof firstName, "String Type");
console.log(typeof age, "Number Type");
console.log(typeof isMale, "Boolean Type");
console.log(typeof dob, "Undefined Type");
console.log(typeof address, "Null Type");